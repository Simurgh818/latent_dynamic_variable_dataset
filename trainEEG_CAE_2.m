function [net, info] = trainEEG_CAE_2(X_train_1D, X_test_1D, cfg)

    arguments
        X_train_1D double 
        X_test_1D  double 
        cfg.bottleneckSize (1,1) double
        cfg.numBands double = 5
        cfg.epochs double = 250
        cfg.batchSize double = 32
        cfg.learnRate double = 1e-3
        cfg.checkpointPath string = ""
        cfg.bandWeights double = [1 1 1 2 3]   % Delta Theta Alpha Beta Gamma
    end
    
    WindowLength = size(X_train_1D, 2);
    TotalChannels = size(X_train_1D, 3);
    NumChannelsPerBand = TotalChannels / cfg.numBands; 
    
    filters_per_band = 64; 
    total_filters = filters_per_band * cfg.numBands;
    
    layers = [
        imageInputLayer([1 WindowLength TotalChannels], ...
            "Normalization", "zscore", ...
            "Name", "input")
        
        groupedConvolution2dLayer([1 64], filters_per_band, cfg.numBands, ...
            "Padding", "same", ...
            "Name", "enc_conv1")
        leakyReluLayer(0.01, "Name", "enc_relu1")
        
        groupedConvolution2dLayer([1 32], filters_per_band, cfg.numBands, ...
            "Padding", "same", ...
            "Name", "enc_conv2")
        leakyReluLayer(0.01, "Name", "enc_relu2")

        % ===== ADDED: Cross-band fusion =====
        convolution2dLayer([1 1], total_filters, ...
            "Padding", "same", ...
            "Name", "cross_band_fusion")
        leakyReluLayer(0.01, "Name", "fusion_relu")
        % batchNormalizationLayer("Name", "fusion_bn")
        
        % ===== CHANGED: dropout 0.5 -> 0.2 =====
        dropoutLayer(0.2, "Name", "dropout_enc")
        
        convolution2dLayer([1 5], cfg.bottleneckSize, ...
            "Padding", "same", ...
            "Name", "bottleneck")
        % batchNormalizationLayer("Name", "bottleneck_bn")
        
        convolution2dLayer([1 1], total_filters, ...
            "Padding", "same", ...
            "Name", "dec_expand")
        leakyReluLayer(0.01, "Name", "dec_relu_expand")
        
        groupedConvolution2dLayer([1 32], filters_per_band, cfg.numBands, ...
            "Padding", "same", ...
            "Name", "dec_conv1")
        leakyReluLayer(0.01, "Name", "dec_relu1")
        
        groupedConvolution2dLayer([1 64], filters_per_band, cfg.numBands, ...
            "Padding", "same", ...
            "Name", "dec_conv2")
        leakyReluLayer(0.01, "Name", "dec_relu2")
        
        groupedConvolution2dLayer([1 1], NumChannelsPerBand, cfg.numBands, ...
            "Padding", "same", ...
            "Name", "reconstruction")

        % ===== CHANGED: weighted band-aware loss =====
        bandWeightedMSELayer( ...
            "band_weighted_loss", ...
            cfg.numBands, ...
            NumChannelsPerBand, ...
            cfg.bandWeights)
    ];

    val_freq = max(1, floor(size(X_train_1D, 4) / cfg.batchSize));
    
    options = trainingOptions("adam", ...
        "MaxEpochs", cfg.epochs, ...
        "MiniBatchSize", cfg.batchSize, ...
        "InitialLearnRate", cfg.learnRate, ...
        "LearnRateSchedule", "piecewise", ...
        "LearnRateDropPeriod", 25, ...
        "LearnRateDropFactor", 0.5, ...
        "L2Regularization", 1e-4, ...
        "Shuffle", "every-epoch", ...
        "Plots", "training-progress", ... % "none" , "training-progress"
        "Verbose", false, ...
        "ValidationData", {X_test_1D, X_test_1D}, ...
        "ValidationFrequency", val_freq, ...
        "ValidationPatience", 30, ...
        "CheckpointPath", cfg.checkpointPath, ...
        "OutputNetwork", "best-validation-loss", ...
        "ExecutionEnvironment", "auto");
    
    [net, info] = trainNetwork(X_train_1D, X_train_1D, layers, options);
end